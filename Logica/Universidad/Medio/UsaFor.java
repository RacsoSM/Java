package Medio;

public class UsaFor {

	public static void main(String[] args) {
	
		System.out.println("=== IMPRIME NUMEROS MENORES A 11 ===");
		for(int k=1;k<11;k=k+1){
			System.out.println(k);
		}
		
		System.out.println("=== IMPRIME NUMEROS PARES MENORES A 11 ===");
		for(int k=2;k<11;k=k+2){
			System.out.println(k);
		}
		
		System.out.println("=== IMPRIME NUMEROS IMPARES DEL 30 AL 50 ===");
		for(int k=31;k<50;k=k+2){
			System.out.println(k);
		}

	}

}
